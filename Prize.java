public class Prize {
    private int x;
    private int y;
    private int pokedollars;

    public Prize(int x, int y, int pokedollars) {
        this.x = x;
        this.y = y;
        this.pokedollars = pokedollars;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getPokedollars() {
        return pokedollars;
    }
}
